var numUm = 4
var numDois = 2

for(let i = 0; i < 1; i++){
    let numTres = numUm + numDois
    console.log(`A soma entre ${numUm} e ${numDois} é igual ${numTres}`)
}
for(let i = 0; i < 1; i++){
    let numTres = numUm - numDois
    console.log(`A subtração entre ${numUm} e ${numDois} é igual ${numTres}`)
}
for(let i = 0; i < 1; i++){
    let numTres = numUm * numDois
    console.log(`A multiplicação entre ${numUm} e ${numDois} é igual ${numTres}`)
}
for(let i = 0; i < 1; i++){
    let numTres = numUm / numDois
    console.log(`A divisão entre ${numUm} e ${numDois} é igual ${numTres}`)
}